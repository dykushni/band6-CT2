const BitmapAttribute *BitmapMapping::GetPngOffset(uint16_t appIndex, uint16_t pngIndex, std::string &binName)
{
    char name[MAX_NAME_LEN] = "gui00.bin";
    if ((appIndex > 0) && (appIndex <= BITMAP_APP_COUNT) &&
        (pngIndex > 0) && (pngIndex <= apps[appIndex - 1].pngCount)) {
        // snprintf_s returns -1 means formating the string failed.
        if (snprintf_s(name, sizeof(name), (sizeof(name) - 1), "gui%.2d.bin", apps[appIndex - 1].binIndex) == -1) {
            return nullptr;
        }

        binName = name;
        return &apps[appIndex - 1].addrs[pngIndex - 1];
    }

    return nullptr;
}
